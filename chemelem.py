def Z(symbol):
    return ELEMENT.index(symbol)

def mass(item):
    if isinstance(item, int):
        return MASS[item]
    else:
        return MASS[ELEMENT.index(item)]

def covalent_radius(item):
    if isinstance(item, int):
        return ATOMIC_RADIUS[item]
    else:
        return ATOMIC_RADIUS[ELEMENT.index(item)]


def chemelem(item):
    if isinstance(item, int):
        return {
            'symbol': ELEMENT[item],
            'Z': item,
            'mass': MASS[item],
            'covalent_radius': COVALENT_RADIUS[item],
        }
    else:
        Z = ELEMENT.index(item)
        return {
            'symbol': item,
            'Z': Z,
            'mass': MASS[Z],
            'covalent_radius': COVALENT_RADIUS[Z],
        }

def main():
    ap = argparse.ArgumentParser(
        prog='chemelem',
        description=__doc__)
    ap.add_argument('element')
    args = ap.parse_args()
    try:
        x = int(args.element)
        args.element = x
    except ValueError:
        pass
    print(chemelem(args.element))

if __name__ == "__main__":
    main()
